    public void autoId()
    {
            try 
            {
                try 
                {
                    Class.forName("com.mysql.jdbc.Driver"); // connecting with Driver
                }   catch (ClassNotFoundException ex){
                        Logger.getLogger(newproduct.class.getName()).log(Level.SEVERE,null,ex);
                    }
                        con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost/warehouseDataBase","root","Me160127@cr7");
                        java.sql.Statement s =con.createStatement();  // creating statement which can send query
                        //Value from customerid panel

                        ResultSet rs = s.executeQuery("Select MAX(idProduct) from product"); // connection allow us to write statement_query, customer ID is always increasing hence MAX
                        rs.next(); // storing the query output in_resul
                        if(rs.getString("MAX(idProduct)")==null){
                            productid.setText("PD001");
                        } else {
                            long id =Long.parseLong(rs.getString("MAX(idProduct)").substring(2,rs.getString("MAX(idProduct)").length()));
                            id++;
                            productid.setText("PD"+String.format("%03d",id));  //%03d means 3 digit format
                    }
            }       catch(SQLException ex){
                        Logger.getLogger(newproduct.class.getName()).log(Level.SEVERE,null,ex);
                    }
    }
    
    


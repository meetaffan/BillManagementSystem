    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

    

        String PRODUCTID = productid.getText();
        String PRODUCTNAME = productname.getText();
        String RATE = rate.getText();
        String DESCRIPTION = description.getText();
        String ACTIVATE = (String)jComboBox1.getSelectedItem();

        try
        {
            Class.forName("com.mysql.jdbc.Driver"); // connecting with Driver
        }   catch (ClassNotFoundException ex){
            Logger.getLogger(newproduct.class.getName()).log(Level.SEVERE,null,ex);
        }

        try {
            con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost/warehouseDataBase","root","Me160127@cr7");  // WTO
        } catch (SQLException ex) {
            Logger.getLogger(newproduct.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            pre = con.prepareStatement("insert into product(idproduct,ProductName,Rate,Description,Activation)values(?,?,?,?,?)"); // names as per SQL table
            pre.setString(1,PRODUCTID);     // my name defined here, not in SQL, SQL name are in above line
            pre.setString(2,PRODUCTNAME);
            pre.setString(3,RATE);
            pre.setString(4,DESCRIPTION);
            pre.setString(5,ACTIVATE);

            pre.executeUpdate();
            JOptionPane.showMessageDialog(null,"Successfully Updated");

        } catch (SQLException ex) {
             Logger.getLogger(newBuyer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveActionPerformed

